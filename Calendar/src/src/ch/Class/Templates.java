package src.ch.Class;

public class Templates {

	protected int X;
	protected int Y;
	protected int X2;
	protected int Y2;
	
	public Templates(int x, int y, int x2, int y2){
		this.X = x;
		this.Y = y;
		this.X2 = x2;
		this.Y2 = y2;
	}
	
	public int getX() {
		return X;
	}
	public void setX(int x) {
		X = x;
	}
	public int getY() {
		return Y;
	}
	public void setZ(int y) {
		Y = y;
	}

	public int getX2() {
		return X2;
	}

	public void setX2(int x2) {
		X2 = x2;
	}

	public int getY2() {
		return Y2;
	}

	public void setY2(int y2) {
		Y2 = y2;
	}
	
}
